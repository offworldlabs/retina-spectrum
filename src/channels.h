// US broadcast channel definitions.
// All TV channels are 6 MHz wide. ATSC pilot = lower_edge + 0.31 MHz = fc - 2.69 MHz.
// FM channels are 200 kHz wide; pilot_mhz = 0.0 — caller substitutes fc_mhz at runtime.
// Channels are identified by their centre broadcast frequency (fc_mhz).

struct Channel {
    const char *band;      // "fm", "vhf_lo", "vhf_hi", "uhf"
    int         number;    // TV: channel number; FM: fc_mhz * 10 (e.g. 881 = 88.1 MHz)
    float       fc_mhz;   // centre frequency (primary identifier)
    float       bw_mhz;   // bandwidth: 0.2 FM, 6.0 TV
    float       pilot_mhz; // ATSC pilot frequency; 0.0 for FM (caller uses fc_mhz instead)
    float       tol_mhz;  // pilot match tolerance: 0.1 FM (±100 kHz), 0.05 TV (±50 kHz)
};

